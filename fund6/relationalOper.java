package fund6;

public class relationalOper {
    public static void main(String[] args) {

        // relational operators

        int a = 10;
        int b = 11;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a <= b);
        System.out.println(a >= b);
    }

}
