package fund1;

public class variables2 {
    public static void main(String[] args) {
        // integer
        int a = 10;
        int b = 10;
        System.out.println(a + b);
        // string
        String str = "chaitanya sai";
        System.out.println(str);
        // double
        double price = 10.20;
        System.out.println(price);
        // update
        a = 20;
        b = 20;
        System.out.println(a + b);
    }
}
